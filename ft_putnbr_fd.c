#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	n = (long)n;
	if (n > 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		ft_putchar_fd((char)(n + '0'), fd);
	}
}
